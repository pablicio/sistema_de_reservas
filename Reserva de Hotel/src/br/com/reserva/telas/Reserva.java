    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // TODO add your handling code here:
        btSalvar.setText("Editar");
        habilitar();
        btExcluir.setEnabled(true);
        accion="editar";

        int fila = tabela.rowAtPoint(evt.getPoint());

        tReserva.setText(tabela.getValueAt(fila, 0).toString());
        tCodHabitacao.setText(tabela.getValueAt(fila, 1).toString());
        tNumero.setText(tabela.getValueAt(fila, 2).toString());
        tCodCliente.setText(tabela.getValueAt(fila, 3).toString());
        tCliente.setText(tabela.getValueAt(fila, 4).toString());
        tCodFuncionario.setText(tabela.getValueAt(fila, 5).toString());
        tFuncionario.setText(tabela.getValueAt(fila, 6).toString());
        
        cbTipoReserva.setSelectedItem(tabela.getValueAt(fila, 7).toString());
        dcfecha_reserva.setDate(Date.valueOf(tabela.getValueAt(fila, 8).toString()));
        dcfecha_ingresa.setDate(Date.valueOf(tabela.getValueAt(fila, 9).toString()));
        dcfecha_salida.setDate(Date.valueOf(tabela.getValueAt(fila, 10).toString()));
        tCustoHospedagem.setText(tabela.getValueAt(fila, 11).toString());
        cbEstado.setSelectedItem(tabela.getValueAt(fila, 12).toString());

    }//GEN-LAST:event_tabelaMouseClicked

