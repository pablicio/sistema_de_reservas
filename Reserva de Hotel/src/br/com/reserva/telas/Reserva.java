    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        if (tCodHabitacao.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar una Habitación");
            tCodHabitacao.requestFocus();
            return;
        }
        if (tCodCliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar un Cliente");
            tCodCliente.requestFocus();
            return;
        }

        if (tCustoHospedagem.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio del Alojamiento");
            tCustoHospedagem.requestFocus();
            return;
        }

        vReserva dts = new vReserva();
        fReserva func = new fReserva();
        
        dts.setCod_habitacao(Integer.parseInt(tCodHabitacao.getText()));
        dts.setCod_cliente(Integer.parseInt(tCodCliente.getText()));
        dts.setCod_funcionario(idusuario);
        
        int seleccionado = cbTipoReserva.getSelectedIndex();
        dts.setTipo_reserva((String) cbTipoReserva.getItemAt(seleccionado));
        
        Calendar cal;
        int d,m,a;
        cal=dcfecha_reserva.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setData_reserva(new Date(a,m,d));
        
        cal=dcfecha_ingresa.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setData_entrada(new Date(a,m,d));
        
        cal=dcfecha_salida.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setData_saida(new Date(a,m,d));
        
        dts.setCusto_hospedagem(Double.parseDouble(tCustoHospedagem.getText()));
        seleccionado = cbEstado.getSelectedIndex();
        dts.setEstado((String) cbEstado.getItemAt(seleccionado));
        
        
        
        
        
        
        
        
        

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue registrada satisfactoriamente");
                mostrar("");
                inhabilitar();
                
                //ocupamos la Habitación alquilada
                fHabitacao func3= new fHabitacao();
                vHabitacao dts3 = new vHabitacao();
                
                dts3.setCod_habitacao(Integer.parseInt(tCodHabitacao.getText()));
                func3.ocupar(dts3);
            }

        }
        else if (accion.equals("editar")){
            dts.setCod_reserva(Integer.parseInt(tReserva.getText()));
            dts.setCod_funcionario(Integer.parseInt(tCodFuncionario.getText()));            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue Editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btSalvarActionPerformed

